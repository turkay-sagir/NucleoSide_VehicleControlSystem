// Bu fonksiyon CAN verisi olup olmadığını kontrol eder ve varsa veriyi UART üzerinden gönderir
void CAN_CheckAndPrint(void)
{
    if (MCP2515_RX_ReceiveMessage(&receivedMessage))
    {
        char msg[128];
        int len = snprintf(msg, sizeof(msg), "CAN ID: 0x%X\r\nDATA: ", receivedMessage.id);
        HAL_UART_Transmit(&huart2, (uint8_t *)msg, len, HAL_MAX_DELAY);

        for (uint8_t i = 0; i < receivedMessage.length && i < 8; i++)
        {
            len = snprintf(msg, sizeof(msg), "%02X ", receivedMessage.data[i]);
            HAL_UART_Transmit(&huart2, (uint8_t *)msg, len, HAL_MAX_DELAY);
        }

        const char *newline = "\r\n\r\n";
        HAL_UART_Transmit(&huart2, (uint8_t *)newline, strlen(newline), HAL_MAX_DELAY);
    }
}

